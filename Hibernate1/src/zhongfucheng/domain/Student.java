package zhongfucheng.domain;

public class Student {
	private Integer sid;
	private Integer age;
	private String  name;
	public  Integer getSid(){
		return sid;
	}
	public void setSid(Integer sid) {
		this.sid = sid;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(int i) {
		this.age = i;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
